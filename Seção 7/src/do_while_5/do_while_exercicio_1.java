package do_while_5;

/*
 * O objetivo deste exercício é imprimir os números de 1 a 100 com a função do-while
 * */

public class do_while_exercicio_1 {

	public static void main(String[] args) {
		
		int numero = 1;
		
		do {
			
			System.out.println(numero);
			
			numero++;
			
		} while (numero <= 100);

	}

}
