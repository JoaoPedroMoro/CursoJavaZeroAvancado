package logica;

public class Constantes {

	// Constantes são valores que não mudam durante a execução do programa.
	// Usamos a palavra-chave "final" para indicar que é um valor fixo, uma constante.
	// Por convenção, constantes são escritas em letra maiúscula.
	public static void main(String[] args) {
		
		final double PI = 3.14159;
		
		final int ANO_ATUAL = 2025;
		
		System.out.println("Valor de PI:" + PI);
		
		System.out.println("Ano atual: " + ANO_ATUAL);

	}

}
