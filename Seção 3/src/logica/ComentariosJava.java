package logica;

public class ComentariosJava {

	public static void main(String[] args) {
		
		// Este é um exemplo de comentário de linha única.
		// Em cima da linha ou ao lado da linha.
		System.out.println("Este é um exemplo de linha única");

		/*
		 * Este é um comentário de múltiplas
		 * linhas
		 * posso ir colocando como eu quiser
		 *  deve estar em cima ou abaixo do código, mas 
		 *  não ao lado */
		System.out.println("Este é um exemplo de comentário de múltiplas linhas.");

	}

}
