package if_2;

public class IF_Exemplo_2 {

	public static void main(String[] args) {
		
		double nota = 6.0;
		
		if (nota >= 7) {
			
			System.out.println("Você passou na prova! =D");
			
		} else if (nota >= 5 ){
			
			System.out.println("Você está de recuperação :C");
			
		} else {
			
			System.out.println("Você não passou na prova! :(");
			
		}

	}

}
