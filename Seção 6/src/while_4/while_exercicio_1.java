package while_4;

/*
 * O objetivo deste programa é imprimir os números de 1 a 100 usando o while.
 * */

public class while_exercicio_1 {

	public static void main(String[] args) {

		int contador = 1;
		
		while (contador <= 100) {
			
			System.out.println(contador);
			
			contador++;
			
		}

	}

}
