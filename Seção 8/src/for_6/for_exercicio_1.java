package for_6;

/*
 * O exercício desse programa consiste em imprimir todos os números impares de 1 até 99 usando a função For.
 * */

public class for_exercicio_1 {

	public static void main(String[] args) {
		
		for (int i = 1 ; i <= 99 ; i++) {
			
			if (i % 2 == 1) {
				
				System.out.println(i);
				
			}
			
		}

	}

}
