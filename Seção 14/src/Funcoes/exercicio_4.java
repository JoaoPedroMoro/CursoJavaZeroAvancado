package Funcoes;

public class exercicio_4 {
	
	public static boolean maiorIdade (int idade) {
		return idade >= 18;
	}

	public static void main(String[] args) {

		System.out.println("Idade 17 é maior de idade? " + maiorIdade(17));
		
		System.out.println("Idade 18 é maior de idade? " + maiorIdade(18));
		
		System.out.println("Idade 19 é maior de idade? " + maiorIdade(19));

	}

}
