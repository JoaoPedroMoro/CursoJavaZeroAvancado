package Funcoes;

public class exercicio_3 {
	
	public static double media (double n1, double n2) {
		
		return (n1 + n2)/2;
		
	}

	public static void main(String[] args) {

		System.out.println("Média de 2 e 5 = " + media(2, 5));
		
		System.out.println("Média de 2.5 e 2.5 = " + media(2.5, 3.5));
		
		System.out.println("Média de 20 e 50 = " + media(20, 50));
		
		System.out.println("Média de 7.1 e 15.8 = " + media(7.1, 15.8));
		

	}

}
